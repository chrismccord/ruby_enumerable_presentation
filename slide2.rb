

# Enumerable is Ruby standard library Mixin

#  - "Provides classes with several traversal, 
#    searching, and sorting methods"

#  - Provides nearly any method a collection would 
#    ever need, ever.

#  - The class need only provide an #each method, 
#    which yields successive members of the collection

# - If Enumerable #max, #min, or #sort is used, 
#   the objects in the collection must also implement a 
#   meaningful <=> operator




